/**
 * <h1>Place</h1>
 * This class enables us to give a unique id to all Depots and Stops.
 */

public class Place {
    private int id;

    /**
     * Getter function for the id.
     * @return int The id of the Place.
     */
    public int getId() {
        return id;
    }

    /**
     * Setter function for the id.
     * @param id The new value of the id.
     */
    public void setId(int id) {
        this.id = id;
    }
}
